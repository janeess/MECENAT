CREATE TABLE PERFORMANCE (
	PERFO_NO NUMBER PRIMARY KEY,
    RENTAL_CODE NUMBER NOT NULL,
	PERFO_TITLE	VARCHAR2(200) NOT NULL,
    GENRE_NAME	VARCHAR2(50) NOT NULL,
    PERFO_EVENT_DATE VARCHAR2(100)  NOT NULL,--7차수정 시작,종료일 컬럼 삭제
	DIRECTOR VARCHAR2(50) NULL,
	CAST VARCHAR2(1000) NULL,
	PERFO_CONTENT VARCHAR2(2000) NULL,
    START_TIME VARCHAR2(50) NULL,
	RUNNING_TIME VARCHAR2(50) NULL,
	AGE_LIMIT NUMBER NULL,
	PERFO_STATUS VARCHAR2(20) DEFAULT '공연예정' CHECK (PERFO_STATUS IN('공연예정','공연중', '공연종료')),
