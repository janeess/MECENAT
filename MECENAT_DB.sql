COMMENT ON COLUMN REVIEW.REVIEW_CONTENT IS '리뷰내용';
COMMENT ON COLUMN REVIEW.WRITE_DATE IS '작성일';
COMMENT ON COLUMN REVIEW.STATUS IS '상태값(Y/N)';

