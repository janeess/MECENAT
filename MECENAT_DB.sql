CREATE TABLE NOTICE_TYPE (
	BOARD_CODE VARCHAR2(10) PRIMARY KEY,
    BOARD_NAME VARCHAR2(30) NOT NULL
);

COMMENT ON COLUMN NOTICE_TYPE.BOARD_CODE IS '게시판유형 코드';
COMMENT ON COLUMN NOTICE_TYPE.BOARD_NAME IS '게시판유형 명칭';

CREATE TABLE NOTICE (
	NOTICE_NO NUMBER PRIMARY KEY NOT NULL,
	BOARD_CODE VARCHAR2(10) NOT NULL,
	USER_ID VARCHAR2(50) NOT NULL,
    NOTICE_TITLE VARCHAR2(120) NOT NULL,
    NOTICE_CONTENT VARCHAR2(2000) NOT NULL,
	ORIGIN_NAME VARCHAR2(100) NULL,
	CHANGE_NAME VARCHAR2(100) NULL,
	NOTICE_DATE DATE DEFAULT SYSDATE,
	NOTICE_COUNT NUMBER NULL,
	STATUS VARCHAR2(1) DEFAULT 'Y',
    FOREIGN KEY (USER_ID) REFERENCES MEMBER(USER_ID),
    FOREIGN KEY (BOARD_CODE) REFERENCES NOTICE_TYPE (BOARD_CODE)
);

COMMENT ON COLUMN NOTICE.NOTICE_NO IS '안내게시글 번호';
COMMENT ON COLUMN NOTICE.BOARD_CODE IS '게시판유형 코드';
COMMENT ON COLUMN NOTICE.USER_ID IS '회원 아이디';
COMMENT ON COLUMN NOTICE.NOTICE_TITLE IS '안내게시판 제목';
COMMENT ON COLUMN NOTICE.NOTICE_CONTENT IS '안내게시판 내용';
COMMENT ON COLUMN NOTICE.ORIGIN_NAME IS '첨부파일 원래이름';
COMMENT ON COLUMN NOTICE.CHANGE_NAME IS '첨부파일 변경이름';
COMMENT ON COLUMN NOTICE.NOTICE_DATE IS '작성일자';
COMMENT ON COLUMN NOTICE.NOTICE_COUNT IS '조회수';
