	PRICE NUMBER NOT NULL,
    PERFO_COUNT NUMBER NULL,
    ORIGIN_NAME VARCHAR2(1000) NULL,
    CHANGE_NAME VARCHAR2(1000) NULL,
    FOREIGN KEY (RENTAL_CODE) REFERENCES RENTAPPLICATION (RENTAL_CODE)
